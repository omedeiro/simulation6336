function dPsidt = eval_dPsidt(i,j,k, x, y1, y2, y3, hx, hy, hz, kappa, N)
dPsidt = zeros(N, N, N);

    %%% ALL POINTS IN CENTER %%%
    if i > 1 && i < N && j > 1 && j < N && k > 1 && k < N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - 2*x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - 2*x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    %%% FACES OFF ORIGIN %%% 
    if i == 1 && j > 1 && j < N && k > 1 && k < N
        dPsidt(i,j,k) = (-x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - 2*x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    if i > 1 && i < N && j==1 && k > 1 && k < N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - 2*x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(-x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    if i > 1 && i < N && j > 1 && j < N && k==1
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - 2*x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - 2*x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(-x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    %%% FACES OFF N,N,N CORNER %%%
    if i == N && j > 1 && j < N && k > 1 && k < N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - x(i,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - 2*x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    if i > 1 && i < N && j==N && k > 1 && k < N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - 2*x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - x(i,j,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    if i > 1 && i < N && j > 1 && j < N && k==N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - 2*x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - 2*x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - x(i,j,k))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    %%%  Z DIRECTED EDGES %%%    
    if i==1 && j==1 && k > 1 && k < N
        dPsidt(i,j,k) = (-x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(-x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    if i==N && j==1 && k > 1 && k < N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - x(i,j,k))/hx^2 ...
                        +(-x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    if i > 1 && i < N && j > 1 && j < N && k > 1 && k < N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - 2*x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - 2*x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
    
    if i > 1 && i < N && j > 1 && j < N && k > 1 && k < N
        dPsidt(i,j,k) = (exp(1i*y1(i-1,j,k))*x(i-1,j,k) - 2*x(i,j,k) + exp(-1i*y1(i,j,k))*x(i+1,j,k))/hx^2 ...
                        +(exp(1i*y2(i,j-1,k))*x(i,j-1,k) - 2*x(i,j,k) + exp(-1i*y2(i,j,k))*x(i,j+1,k))/hy^2 ...
                        +(exp(1i*y3(i,j,k-1))*x(i,j,k-1) - 2*x(i,j,k) + exp(-1i*y3(i,j,k))*x(i,j,k+1))/hz^2 ...
                        + (1-x(i,j,k)*conj(x(i,j,k)))*x(i,j,k); 
    end
end

